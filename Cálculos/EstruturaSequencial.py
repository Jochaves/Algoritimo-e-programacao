#Escreva um programa em Python que permite ao usuário digitar dois números inteiros e exibir o resultado para cada uma 
#das seguintes operações, nesta ordem soma, subitração, multiplicação, divisão truncada, resto e exponenciação 

a = int(input("Digite o primeiro número:"))
